#!/usr/bin/env python
# -*- coding:utf-8 -*-


# * 进行参数解包---------------------------------------------------------------
# 这种解包方式支持任何可迭代对象
def func(a, b, c, d):
    print(a, b, c, d)


args = (1, 2, 3, 4)
func(*args)
# 相当于 func(1, 2, 3, 4)


# ** 进行参数解包--------------------------------------------------------------
args = {'a': 1, 'b': 2, 'c': 3, 'd': 4}
func(**args)
# 相当于 func(a=1, b=2, c=3, d=4)

# 混合使用--------------------------------------------------------------
func(*(1, 2), **{'d': 4, 'c': 3})
func(1, *(2, 3), **{'d': 4})
func(1, *(2, 3), d=4)
func(1, c=3, *(2,), **{'d': 4})
