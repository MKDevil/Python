# 1、while和for循环的区别？
# 都可以用于循环，但while是更加通用的循环模式，for更常用与迭代和遍历

# 2、break和continue之间的区别？
# break触发后，直接跳出当前的循环
# continue触发后，忽略后续语句，从当前循环的首行继续执行，迭代对象会迭代到下一个

# 3、一个循环语句的else分句何时执行？
# 当不满足循环条件时执行，即循环结束时执行
# 可以被break语句跳过

